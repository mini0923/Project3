package main;

public class Ex3 {

	public static void main(String[] args) {

		int result1 = 10 + 2;			// 더하기
		int result2 = 10 - 2;			// 빼기
		int result3 = 10 * 2;			// 곱하기
		int result4 = 10 / 2;			// 나누기
		int result5 = 10 % 2;			// 나머지
		
		System.out.println(result1);	// 결과 12
		System.out.println(result2);	// 결과 8
		System.out.println(result3);	// 결과 20
		System.out.println(result4);  // 결과 5
		System.out.println(result5);  // 결과 0
		
		
		
	}

}
